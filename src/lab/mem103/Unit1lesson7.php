<dt><b><a name="classification">7.4 &nbsp;&nbsp;&nbsp;Classification of cast iron</a></b></dt>
<dd><p>
<b>On what factors the various irons are classified?</b><br />
The various irons can be classified on the form of their graphite and the type of matrix structure in which it is embedded.<br /><br />
<b>In what form the carbon is present in steel and cast iron?</b><br />
While carbon in ordinary steel exists as cementite (Fe3C), in cast iron it occurs in two forms:<br />
stable form-graphite and unstable form-cementite, analysed as combined carbon.<br /><br />
<b>Classify cast irons</b><br />
The first classification of cast iron was based on its fracture.<br /><br />
<b>Two types of iron were initially recognised</b>:<br /> 
i) <b>White iron</b>: Exhibits a white, crystalline fracture surface because fracture occurs along the iron carbide plates; it is the result of metastable solidification (Fe3C eutectic).<br /><br />
ii) <b>Gray iron</b>: Exhibits a gray fracture surface because fracture occurs along the graphite plates (flakes); it is the result of stable solidification (Gr eutectic).<br /><br />
