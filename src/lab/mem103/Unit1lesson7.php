<dt><b><a name="introduction">7.0 &nbsp;&nbsp;&nbsp;Ferrous Metals</a></b></dt>
<dd><p>
Ferrous metals are metals that contain iron as the base metal. Almost all ferrous metals are magnetic. The basic ferrous metal form is pig iron.</p></dd><br />
<dt><b><a name="iron">7.1 &nbsp;&nbsp;&nbsp;Iron</a></b></dt>
<dd><p>
Iron is a lustrous, ductile, malleable, silver-gray fusible element that is the cheapest and most used among all metals. It is known to exist in four distinct crystalline forms. In the Earth iron occurs mainly in iron-oxide ores. One of these ores magnetite (Fe3O4), named for its property of magnetism. Hematite (Fe2O3) is the most plentiful ore, but it contains less iron than magnetite. The various alloys of iron, after undergoing certain processes, are pig iron, gray cast iron, white iron, white cast iron, malleable cast iron, wrought iron, alloy steel, and carbon steel. Pig iron is used to produce steel.<br /><br />
<b>7.1.1	Properties of Iron</b><br />
Boiling point 2,7500C<br />
Melting point 1,5350C<br />
Specific gravity 7.874<br /><br />
<b>7.1.2	Uses</b><br />
Iron is the basic material used in making steel and cast iron.<br />
An oxide of iron called Venetian red is used as a pigment in paint for bridges and railroad cars.<br />
Iron compound called Prussian blue is a pigment, which is used as Laundry bluing and by architects and engineers for blueprints.<br />
Iron cores are used in electromagnets for motors, telephones, and other equipment.<br /><br />
<b>7.1.3	Manufacture of Iron</b><br />
<b>Blast furnace</b><br />
The blast furnace is used to produce cast iron or steel from iron oxides. The purpose of a blast furnace is to chemically reduce and physically convert iron oxides into liquid iron called "hot metal" (pig iron). <br /><br />
<b>Wrought iron</b><br />
Wrought iron is almost pure iron that has had most of its carbon removed. It is made from pig iron in a puddling furnace and has a carbon content of less than 0.08 percent.<br />
<b>Manufacture of wrought iron</b><br />
Wrought iron is manufactured from pig iron in a puddling furnace.<br />
<b>Characteristics</b><br />
Wrought iron is ductile and corrosion resistant<br />
It becomes soft at white heat.<br />
<b>Uses</b> - Wrought iron is used for porch railings, fencing, farm implements, nails, barbed wire, chains, furniture, decorations bolts and nuts, chain, railway coupling, water and steam pipes.<br />
<b>Capabilities</b> - Wrought iron can be gas and arc welded, machined, forged, plated, and is easily formed.<br />
<b>Limitations</b> - Wrought iron has low hardness and low fatigue strength
</p></dd><br />
<dt><b><a name="cast">7.2 &nbsp;&nbsp;&nbsp;Cast Iron</a></b></dt>
<dd><p>
Cast iron (CI) is a ferrous metal, an alloy of iron, carbon (2.1 to 4.5%) and silicon (3.5%). Cast iron is a hard and brittle material. Graphite (carbon) is present in cast iron in free form and it adds self-lubricating properties to cast iron. Cast iron has excellent vibration damping property. It is, therefore, extensively used for making machine beds and frames. Cast iron has good compressive strength, but is weak in tension. Different types of cast irons are used in engineering applications. A few of them are gray cast iron, malleable cast iron, spheroidal-graphite cast iron, etc., each having different composition and percentage of alloying elements.<br />
Gray cast iron has good wear resistance and is used for castings of machine tool guideways, where continuous lubrication is not always possible. Malleable cast iron has more tensile, impact and fatigue strength than gray cast iron. Application of malleable cast iron includes parts for agricultural and textile machinery. Spheroidal-graphite (SG) cast iron achieves the strength, toughness and ductility of steel. SG cast iron is widely used in the manufacturing of crankshafts and connecting rods, gearbox housings etc
</p></dd><br />
<dt><b><a name="steel">7.3 &nbsp;&nbsp;&nbsp;Steel</a></b></dt>
<dd><p>
Steel is the most common engineering material used for a wide range of applications from utensils to machine parts to cutting tools. Steel is an alloy of iron and carbon. In addition, it may contain other alloying elements such as manganese, silicon, chromium and copper. Steels are classified based on the percentage of carbon present, into three groups viz. low carbon steel (0.05 to 0.3%C), medium carbon steel (0.3 to 0.7%C) and high carbon steel (0.7 to 1.5%C). The low carbon steel is also known as mild steel. 
