<dt><b><a name="introduction">1.0 &nbsp;&nbsp;&nbsp;Introduction</a></b></dt>
<dd><p>
Irrespective of what the global economic conditions may be, the manufacturing sector remains a key part of any national economy; manufacturer, is a strategic enabler of growth and job creation, playing a decisive part in any countries socio-economic development. Manufacturing has had a direct impact on job creation by spawning several related production and service industries supporting its manufacturing needs.<br /><br />
The classical breakdown of all economic sectors follows:<br />
<b>Primary</b>: Involves the retrieval and production of raw materials, such as corn, coal, wood and iron. (A coal miner and a fisherman would be workers in the primary sector).<br />
<b>Secondary</b>: Involves the transformation of raw or intermediate materials into goods e.g. manufacturing steel into cars, or textiles into clothing. (A builder and a dressmaker would be workers in the secondary sector.)<br />
<b>Tertiary</b>: Involves the supplying of services to consumers and businesses, such as baby-sitting, cinema and banking. (A shopkeeper and an accountant would be workers in the tertiary sector.)
</p></dd><br />
<dt><b><a name="major">1.1 &nbsp;&nbsp;&nbsp;Major Technological Developments</a></b></dt>
<dd><p>
The commencement of the Industrial Revolution is closely linked to a small number of innovations, beginning in the second half of the 18th century. By the 1830s the following gains had been made in important technologies:<br /><br />
<b>1.1.1 Textiles</b> - Mechanized cotton spinning powered by steam or water increased the output of a worker by a factor of about 1000. The power loom increased the output of a worker by a factor of over 40. The cotton gin increased productivity or removing seed from cotton by a factor of 50. Large gains in productivity also occurred in spinning and weaving of wool and linen, but they were not as great as in cotton.<br /><br />
<b>1.1.2 Steam Power</b> - The efficiency of steam engines increased so that they used between one-fifth and one-tenth as much fuel. The adaption of stationary steam engines to rotary motion made them suitable for industrial uses. The high pressure engine had a high power to weight ratio, making it suitable for transportation. Steam power underwent a rapid expansion after 1800.<br /><br />
<b>1.1.3 Iron Making</b> - The substitution of coke for charcoal greatly lowered the fuel cost of pig iron and wrought iron production. Using coke also allowed larger blast furnaces, resulting in economies of scale. The cast iron blowing cylinder was first used in 1760. It was later improved by making it double acting, which allowed higher furnace temperatures. The puddling process produced a structural grade iron at a lower cost than the previous processes. The rolling mill was fifteen times faster than hammering wrought iron. Hot blast (1829) greatly increased fuel efficiency in iron production in the following decades.
</p></dd><br />
<dt><b><a name="sustainable">1.2 &nbsp;&nbsp;&nbsp;Sustainable Manufacturing</a></b></dt>
<dd><p>
<b>United Nations' definition of sustainable manufacturing</b><br /> 
Sustainable "meets the needs of the present without compromising the ability of future generations to meet their own needs".<br /><br /> 
"Sustainable manufacturing is defined as the creation of manufactured products that use processes that are non-polluting, conserve energy and natural resources, and are economically sound and safe for employees, communities, and consumers".<br /><br />
Sustainable manufacturing includes the manufacturing of "sustainable" products and the sustainable manufacturing of all products. The former includes manufacturing of renewable energy, energy efficiency, green building, and other "green" & social equity-related products.<br /><br />
<b>A technical version of this definition is as follows</b>:<br />
"Sustainable manufacturing is a systems approach for the creation and distribution (supply chain) of innovative products and services that: minimizes resources (inputs such as materials, energy, water, and land); eliminates toxic substances; and produces zero waste that in effect reduces green house gases, e.g., carbon intensity, across the entire life cycle of products and services."<br /><br />
To achieve sustainability, products, processes, and services should meet the challenges not only related to their functions and performance but also to environment, economy, and social issues. Currently, researchers from different perspectives using various approaches are addressing these challenges. Companies interested in developing sustainable products should be sensitive to sustainability-related standards, design, and manufacturing techniques and tools.<br /><br />
Ensuring a sustainable future requires an integrated system of systems approach. Interlinked pathways of interaction at various levels characterize such systems. These levels span technical, economic, ecological, and societal issues. The interactions within and across these levels are critical to the fundamental understanding of sustainable design and manufacturing, because addressing any one of the issues in isolation could result in unintended consequences.
</p></dd><br />
